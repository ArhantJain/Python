## Arithmetic  Operator ##

num1 = 10
num2 = 20
print("Num1 + Num2=",num1 + num2)
print("Num1 - Num2=",num1 - num2)
print("Num * Num2 = ",num1*num2)
print("Num / Num2 = ",num1/num2)

print(5**3) # Exponential function
print(20%3) # Gives remainder
print(22//7)  # Greatest integer less than or equal to
print(3.8//2)

## Assignment Operator ##

num3 = num1 + num2
print(num3)
num3 += num1
print(num3) # 30 + 10

## Comparison Operator


print("Is num 3 > num2 ?",num3 > num2)
print("Is num2 = num3?", num2 == num3)
print("is num1 != num2?", num1 != num2)

## Logical Operator

x = True
y = False


print("X and Y " , x and y)
print("X or Y ", x or y)
print("Not of X", not x)

# Bitwise Operator

num4 = 6 # 110
num5 = 2 # 10

print( " Bitwise and =", num4 & num5)
print( " Bitwise OR  =", num4 | num5)
print( " Bitwise XOR =", num4 ^ num5)
print( " Bitwise Right shift =", num4 >> 2)
print( " Bitwise Left Shift =", num5 << 2)

# Identity Operator
print(x is 5)
print ( x is not 5)

# Membership Operator
X = [ 1,2,3,4,5]
print(3 in X)
print( 10 not in X)













































